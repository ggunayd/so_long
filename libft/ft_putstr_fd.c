#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	idx;

	if (!s)
		return ;
	idx = 0;
	while (s[idx])
	{
		ft_putchar_fd(s[idx], fd);
		idx++;
	}
}
